#Escreva um programa que peça ao usurario para inserir tres numeros inteiro, soma os dois primeiros e multiplique esse total pelo terceiro
a = int (input ("Digite o primeiro numero: "))
b = int (input ("Digite o segundo numero: "))
c = int (input ("Digite o terceiro numero: "))
resul = (a + b) * c
print ("O total é:", resul)